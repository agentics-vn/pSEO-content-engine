/** Deterministic review sampling: same item_key always samples the same way. */
export function reviewSampleHit(itemKey: string, pct: number): boolean {
  if (pct >= 100) return true;
  if (pct <= 0) return false;
  let h = 0;
  for (const c of itemKey) h = (h * 31 + c.charCodeAt(0)) >>> 0;
  return h % 100 < pct;
}

// ── faq_shape gate (config-driven, merged with the generic per-item gates) ───

export function gateFaqShape(
  output: Record<string, unknown>,
  cfg: { count?: number; answers_must_contain?: string[] } | undefined,
): GateResult {
  const faqs = output.faqs as Array<{ q?: unknown; a?: unknown }> | undefined;
  const viol: string[] = [];
  if (!Array.isArray(faqs)) {
    viol.push('faqs missing or not an array');
  } else {
    if (cfg?.count !== undefined && faqs.length !== cfg.count) {
      viol.push(`expected exactly ${cfg.count} faqs, got ${faqs.length}`);
    }
    for (const f of faqs) {
      if (typeof f?.q !== 'string' || typeof f?.a !== 'string') viol.push('faq entry missing q/a');
    }
    for (const token of cfg?.answers_must_contain ?? []) {
      if (!faqs.some((f) => typeof f?.a === 'string' && f.a.includes(token))) {
        viol.push(`no faq answer contains "${token}"`);
      }
    }
  }
  return { gate: 'faq_shape', severity: 'fail', passed: viol.length === 0,
           detail: viol.join('; ') || undefined };
}

