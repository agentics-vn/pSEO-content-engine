// ── Schema handling ──────────────────────────────────────────────────────────

/** Constraint keywords Anthropic's strict mode rejects inside input_schema. */
const STRICT_REJECTED_KEYWORDS = [
  'minItems', 'maxItems', 'minLength', 'maxLength', 'pattern',
  'minimum', 'maximum', 'exclusiveMinimum', 'exclusiveMaximum', 'multipleOf',
] as const;

/**
 * Deep-clone the template's output_schema and remove every constraint keyword
 * strict mode rejects. Also enforce what strict mode REQUIRES on every object:
 * `additionalProperties: false` and a `required` array (defaulting to all
 * declared properties).
 *
 * The dropped constraints are NOT lost — constraintNotes() re-issues them as
 * prose (ground rule 5; skipping that failed ~⅓ of items in the reference
 * project's first full batch).
 */
export function stripForStrict(schema: unknown): unknown {
  if (schema === null || typeof schema !== 'object') return schema;
  if (Array.isArray(schema)) return schema.map(stripForStrict);

  const src = schema as Record<string, unknown>;
  const out: Record<string, unknown> = {};
  for (const [k, v] of Object.entries(src)) {
    if ((STRICT_REJECTED_KEYWORDS as readonly string[]).includes(k)) continue;
    out[k] = stripForStrict(v);
  }
  if (out.type === 'object' && typeof out.properties === 'object' && out.properties !== null) {
    out.additionalProperties = false;
    if (!Array.isArray(out.required)) out.required = Object.keys(out.properties);
  }
  return out;
}

export interface Guards {
  [gate: string]: Record<string, unknown> | undefined;
}

/**
 * Render every bound that stripForStrict dropped — plus the length /
 * required-mention / faq-shape guards — as plain-language RÀNG BUỘC lines.
 * Placeholder tokens ({lifePath}, {linking}, …) are left intact; the caller
 * runs fillTemplate over the final prompt, resolving them from input_data.
 * Generated by walking the config, never hardcoded per tenant.
 */
export function constraintNotes(schema: unknown, guards: Guards): string {
  const lines: string[] = [];

  const length = guards.length as { fields?: Record<string, [number, number]> } | undefined;
  for (const [field, [min, max]] of Object.entries(length?.fields ?? {})) {
    const el = /^(.+)\.(\d+)$/.exec(field); // "phanTich.0" → "phanTich (đoạn 1)"
    const label = el ? `${el[1]} (đoạn ${Number(el[2]) + 1})` : field;
    lines.push(`- ${label}: ${min}–${max} ký tự`);
  }

  // Array bounds from the ORIGINAL (un-stripped) schema.
  const props = (schema as { properties?: Record<string, Record<string, unknown>> })?.properties ?? {};
  for (const [field, def] of Object.entries(props)) {
    if (def.type !== 'array') continue;
    const min = def.minItems as number | undefined;
    const max = def.maxItems as number | undefined;
    if (min !== undefined && min === max) lines.push(`- ${field}: ĐÚNG ${min} phần tử`);
    else if (min !== undefined || max !== undefined) {
      lines.push(`- ${field}: ${min ?? 'không giới hạn dưới'}–${max ?? 'không giới hạn trên'} phần tử`);
    }
  }

  const faq = guards.faq_shape as { count?: number; answers_must_contain?: string[] } | undefined;
  if (faq?.count !== undefined) lines.push(`- faqs: ĐÚNG ${faq.count} phần tử`);
  for (const token of faq?.answers_must_contain ?? []) {
    lines.push(`- faqs: ít nhất một câu trả lời phải nêu đúng con số ${token}`);
  }

  const mentions = guards.required_mentions as
    | { rules?: Array<{ field: string; must_contain: string[] }> }
    | undefined;
  for (const rule of mentions?.rules ?? []) {
    lines.push(`- ${rule.field}: phải chứa ${rule.must_contain.join(' và ')}`);
  }

  const banned = guards.banned_phrases as { list?: string[] } | undefined;
  if (banned?.list?.length) {
    lines.push(`- TUYỆT ĐỐI KHÔNG dùng các cụm: ${banned.list.map((p) => `"${p}"`).join(', ')}`);
  }

  const entity = guards.entity_consistency as { pattern?: string; allowed?: string[] } | undefined;
  if (entity?.pattern && entity.allowed?.length) {
    lines.push(`- CHỈ được nhắc tới các thực thể sau (lấy từ dữ kiện): ${entity.allowed.join('; ')}. TUYỆT ĐỐI KHÔNG bịa thực thể khác cùng loại.`);
  }

  if (lines.length === 0) return '';
  return ['RÀNG BUỘC ĐỘ DÀI & SỐ LƯỢNG — tuân thủ tuyệt đối:', ...lines].join('\n');
}

/** Look up a dot path ("lp.archetype") in input_data. */
function lookupPath(data: Record<string, unknown>, path: string): unknown {
  let cur: unknown = data;
  for (const part of path.split('.')) {
    if (cur === null || typeof cur !== 'object') return undefined;
    cur = (cur as Record<string, unknown>)[part];
  }
  return cur;
}

function renderValue(v: unknown): string {
  if (Array.isArray(v)) return v.map(renderValue).join(', ');
  if (v === null || v === undefined) return '';
  return String(v);
}

/**
 * Fill {placeholder} tokens (dot paths allowed) from input_data. Unknown
 * tokens THROW — a template referencing a fact that doesn't exist must fail
 * the generation, not ship a literal "{lifePath}" into prose.
 */
export function fillTemplate(template: string, data: Record<string, unknown>): string {
  return template.replace(/\{([a-zA-Z_][a-zA-Z0-9_.]*)\}/g, (_, path: string) => {
    const v = lookupPath(data, path);
    if (v === undefined) throw new Error(`[prose-generate] template placeholder {${path}} not found in input_data`);
    return renderValue(v);
  });
}

/**
 * Resolve guard placeholder tokens from input_data so the generic gates never
 * learn the domain: {"must_contain": ["{linking}"]} → ["4"]. Applied to every
 * string anywhere in the guards JSON.
 */
export function resolveGuards(guards: Guards, data: Record<string, unknown>): Guards {
  const resolve = (v: unknown): unknown => {
    if (typeof v === 'string') {
      return v.replace(/\{([a-zA-Z_][a-zA-Z0-9_.]*)\}/g, (m, path: string) => {
        const found = lookupPath(data, path);
        return found === undefined ? m : renderValue(found);
      });
    }
    if (Array.isArray(v)) return v.map(resolve);
    if (v !== null && typeof v === 'object') {
      return Object.fromEntries(Object.entries(v as Record<string, unknown>).map(([k, x]) => [k, resolve(x)]));
    }
    return v;
  };
  return resolve(guards) as Guards;
}

/**
 * Cheap second line of defense (§6.4): strict mode + constraint notes should
 * make this a no-op, but re-parse stringified arrays/objects back into real
 * ones when the schema expects them.
 */
export function coerceToSchema(raw: unknown, schema: unknown): unknown {
  const def = schema as Record<string, unknown> | null;
  if (def === null || typeof def !== 'object') return raw;

  if ((def.type === 'array' || def.type === 'object') && typeof raw === 'string') {
    try {
      const parsed = JSON.parse(raw);
      return coerceToSchema(parsed, def);
    } catch {
      return raw;
    }
  }
  if (def.type === 'array' && Array.isArray(raw)) {
    return raw.map((item) => coerceToSchema(item, def.items));
  }
  if (def.type === 'object' && raw !== null && typeof raw === 'object' && !Array.isArray(raw)) {
    const props = (def.properties ?? {}) as Record<string, unknown>;
    const out: Record<string, unknown> = { ...(raw as Record<string, unknown>) };
    for (const [k, propDef] of Object.entries(props)) {
      if (k in out) out[k] = coerceToSchema(out[k], propDef);
    }
    return out;
  }
  return raw;
}

/**
 * Minimal structural validator over the subset of JSON Schema the templates
 * use (type/object/array/required/additionalProperties/enum). Length and
 * numeric bounds are the length/numeric gates' job, not this one's.
 */
export function validateSchema(value: unknown, schema: unknown, path = '$'): string[] {
  const def = schema as Record<string, unknown> | null;
  if (def === null || typeof def !== 'object') return [];
  const errors: string[] = [];
  const t = def.type as string | undefined;

  const typeOk = (): boolean => {
    switch (t) {
      case 'object': return value !== null && typeof value === 'object' && !Array.isArray(value);
      case 'array': return Array.isArray(value);
      case 'string': return typeof value === 'string';
      case 'number': return typeof value === 'number';
      case 'integer': return typeof value === 'number' && Number.isInteger(value);
      case 'boolean': return typeof value === 'boolean';
      case 'null': return value === null;
      default: return true;
    }
  };
  if (!typeOk()) {
    errors.push(`${path}: expected ${t}, got ${Array.isArray(value) ? 'array' : typeof value}`);
    return errors;
  }

  if (Array.isArray(def.enum) && !def.enum.some((e) => e === value)) {
    errors.push(`${path}: not in enum`);
  }

  if (t === 'object') {
    const obj = value as Record<string, unknown>;
    const props = (def.properties ?? {}) as Record<string, unknown>;
    for (const req of (def.required as string[] | undefined) ?? []) {
      if (!(req in obj)) errors.push(`${path}.${req}: required property missing`);
    }
    if (def.additionalProperties === false) {
      for (const k of Object.keys(obj)) {
        if (!(k in props)) errors.push(`${path}.${k}: additional property not allowed`);
      }
    }
    for (const [k, propDef] of Object.entries(props)) {
      if (k in obj) errors.push(...validateSchema(obj[k], propDef, `${path}.${k}`));
    }
  }
  if (t === 'array' && def.items !== undefined) {
    (value as unknown[]).forEach((item, i) => errors.push(...validateSchema(item, def.items, `${path}[${i}]`)));
  }
  return errors;
}

