/**
 * prose-generate — pure logic (no Deno APIs, no secrets). The Deno.serve
 * wiring and the ONLY code path holding ANTHROPIC_API_KEY live in index.ts.
 *
 * Everything here is deterministic and unit-tested:
 *   stripForStrict     schema sent to the API (strict mode rejects bounds)
 *   constraintNotes    the REQUIRED plain-language re-issue of those bounds
 *   fillTemplate       {placeholder} → input_data value (dot paths, arrays)
 *   resolveGuards      guard tokens ({linking} → the number) so gates stay
 *                      domain-blind
 *   coerceToSchema     cheap second line of defense after strict mode
 *   validateSchema     structural check mirroring what strict mode enforced
 *   generateItem       the per-item flow with injected db/llm deps
 */

