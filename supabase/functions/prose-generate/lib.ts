/** Ceiling for auto-sized and retry-bumped output budgets (well under model caps). */
export const MAX_OUTPUT_TOKENS_CAP = 16000;

const STUB_MARKERS = ['placeholder', 'lorem ipsum', 'tbd', 'todo', 'n/a', 'xxx'];

/**
 * Cheap detector for a bailed generation: the model filled the forced tool with
 * stub values (the observed "placeholder"×12 failure), left an unresolved
 * {token}, or nearly every string field is identical. Not a quality judge — it
 * only catches degenerate shells so P1 can retry them instead of burning a
 * review slot.
 */
export function isDegenerate(output: unknown): boolean {
  if (!output || typeof output !== 'object') return true;
  const strings: string[] = [];
  const walk = (v: unknown): void => {
    if (typeof v === 'string') strings.push(v);
    else if (Array.isArray(v)) v.forEach(walk);
    else if (v && typeof v === 'object') Object.values(v).forEach(walk);
  };
  walk(output);
  if (strings.length === 0) return true;
  let stubbed = 0;
  for (const s of strings) {
    if (/\{[a-zA-Z_][a-zA-Z0-9_.]*\}/.test(s)) return true; // unresolved {token} leaked into output
    if (STUB_MARKERS.includes(s.trim().toLowerCase())) stubbed++;
  }
  if (stubbed >= 2) return true;
  const distinct = new Set(strings.map((s) => s.trim()));
  if (strings.length >= 4 && distinct.size <= 2) return true; // near-all-identical shell
  return false;
}

/**
 * Floor the per-item output budget on the template's OWN length bounds so a flat
 * max_tokens can't truncate a long schema. Σ(field max code points) + FAQ + JSON
 * overhead, converted at a conservative chars/token ratio, + margin. The caller
 * takes max(template.max_tokens, this) — a generous ceiling costs nothing (you
 * pay for tokens produced, not the cap).
 */
export function estimateOutputTokens(template: Pick<TemplateRow, 'guards'>): number {
  const lengthFields = (template.guards?.length as { fields?: Record<string, [number, number]> } | undefined)?.fields ?? {};
  let chars = 0;
  for (const bounds of Object.values(lengthFields)) {
    if (Array.isArray(bounds) && typeof bounds[1] === 'number') chars += bounds[1];
  }
  const faq = template.guards?.faq_shape as { count?: number } | undefined;
  chars += (faq?.count ?? 0) * 700; // q + a per faq, generous
  chars += 600;                     // JSON keys/braces + fields without a length bound
  const tokens = Math.ceil(chars / 1.3); // ~1.3 chars/token, conservative for Vietnamese
  return Math.ceil(tokens * 1.15);       // margin
}

/** Short audit hash for a site persona (null when there is none). */
export async function personaHashOf(persona: string | null): Promise<string | null> {
  const trimmed = persona?.trim();
  if (!trimmed) return null;
  return (await sha256Hex(trimmed)).slice(0, 12);
}

/** Auto-sized output budget: never below the template's explicit value, and the
 *  cap bounds only the schema-derived estimate (so a template that deliberately
 *  sets a very high max_tokens is honored, not silently lowered to the cap). */
export function sizedMaxTokens(template: TemplateRow): number {
  return Math.max(template.max_tokens, Math.min(MAX_OUTPUT_TOKENS_CAP, estimateOutputTokens(template)));
}

/** Bump a budget for a retry without ever lowering it (the cap can't shrink an
 *  already-larger explicit budget). */
export function bumpMaxTokens(current: number, factor = 1.7): number {
  return Math.max(current, Math.min(MAX_OUTPUT_TOKENS_CAP, Math.round(current * factor)));
}

/**
 * Build the LLM request for one item: constraint notes, few-shots, strict schema.
 * Shared by sync generate, batch submit, and dry-run. `maxTokensOverride` lets
 * P1 re-issue a truncated item with a bumped budget. `persona` is the SITE-level
 * doctrine (sites.persona) prepended before the template's own system_prompt —
 * one doctrine per site, inherited by every template, so persuasion/voice rules
 * never get copy-pasted per template. Null/empty persona → byte-identical
 * behavior to pre-persona builds (and the T1 cached prefix stays byte-identical
 * per (template, persona), so caching is unaffected).
 */
export function buildItemLlmRequest(
  item: Pick<ItemRow, 'item_key' | 'input_data'>,
  template: TemplateRow,
  opts?: { maxTokensOverride?: number; persona?: string | null },
): LlmRequest {
  const notes = constraintNotes(template.output_schema, template.guards);
  const withNotes = template.user_template.includes('{constraint_notes}')
    ? template.user_template.replace('{constraint_notes}', notes)
    : `${template.user_template}\n\n${notes}`;
  const userPrompt = fillTemplate(withNotes, item.input_data);

  const persona = typeof opts?.persona === 'string' ? opts.persona.trim() : '';
  let system = persona ? `${persona}\n\n${template.system_prompt}` : template.system_prompt;
  const fewShots = (template.few_shots ?? []).filter(
    (shot) => !(shot && typeof shot === 'object' && (shot as { item_key?: string }).item_key === item.item_key),
  );
  if (fewShots.length > 0) {
    system += '\n\nVÍ DỤ THAM KHẢO (giọng văn & cấu trúc — KHÔNG chép lại nội dung, dữ kiện thuộc về tổ hợp khác):\n' +
      fewShots.map((s, i) => `--- Ví dụ ${i + 1} ---\n${JSON.stringify((s as { output?: unknown }).output ?? s)}`).join('\n');
  }

  return {
    model: template.model,
    system,
    userPrompt,
    toolSchema: stripForStrict(template.output_schema) as Record<string, unknown>,
    temperature: template.temperature,
    maxTokens: opts?.maxTokensOverride ?? sizedMaxTokens(template),
  };
}

/**
 * Persist gates, status, and token usage after an LLM result (sync or batch).
 */
export async function finalizeItemFromLlm(
  deps: Pick<GenerateDeps, 'saveResult' | 'addJobUsage' | 'getJobReviewPct'>,
  item: ItemRow,
  template: TemplateRow,
  llmResult: LlmResult,
  opts: {
    mode?: 'generate' | 'regenerate';
    usageChannel: 'batch' | 'sync';
    /** Audit stamp: first 12 hex of sha256(site persona) the item was generated
     *  under, so "which doctrine produced this page" stays answerable after the
     *  (mutable) persona changes. Absent when the site has no persona. */
    personaHash?: string | null;
  },
): Promise<{ status: string; gates: GateResult[] }> {
  await deps.addJobUsage(item.job_id, llmResult.tokensIn, llmResult.tokensOut, opts.usageChannel);

  const output = coerceToSchema(llmResult.output, template.output_schema) as Record<string, unknown>;
  const gates = assembleItemGates(output, template, item.input_data);

  const reviewPct = await deps.getJobReviewPct(item.job_id);
  const anyFail = hasFailingGate(gates);
  const anyFlag = gates.some((g) => g.severity === 'flag' && !g.passed);
  const sampled = reviewSampleHit(item.item_key, reviewPct);
  const status = anyFail ? 'failed_validation' : anyFlag || sampled ? 'flagged' : 'generated';

  const tokensIn = (item.tokens_in ?? 0) + llmResult.tokensIn;
  const tokensOut = (item.tokens_out ?? 0) + llmResult.tokensOut;

  await deps.saveResult(item, {
    output,
    status,
    validation: {
      gates,
      review_sampled: sampled,
      ...(opts.personaHash ? { persona_hash: opts.personaHash } : {}),
    },
    regen_count: opts.mode === 'regenerate' ? item.regen_count + 1 : item.regen_count,
    tokens_in: tokensIn,
    tokens_out: tokensOut,
    usage_channel: opts.usageChannel,
  });

  return { status, gates };
}

/**
 * Assemble the per-item gate results for an output against a template + its
 * input_data: schema, the generic per-item gates over resolved guards, and
 * faq_shape. Used by generateItem AND by prose-admin's edit handler (so a
 * reviewer edit is re-gated — the fail-gate publish guarantee must hold for
 * edited_output, not just the original output). Batch gates are separate.
 */
export function assembleItemGates(
  output: Record<string, unknown>,
  template: { output_schema: Record<string, unknown>; guards: Record<string, unknown> },
  inputData: Record<string, unknown>,
): GateResult[] {
  const guards = template.guards as Guards;
  const resolved = resolveGuards(guards, inputData);
  const gates: GateResult[] = [];
  if (guards.schema !== undefined) {
    const errs = validateSchema(output, template.output_schema);
    gates.push({ gate: 'schema', severity: 'fail', passed: errs.length === 0,
                 detail: errs.slice(0, 8).join('; ') || undefined });
  }
  const computedCfg = guards.numeric_consistency as { computed?: string[] } | undefined;
  const computed: Record<string, number> = {};
  for (const field of computedCfg?.computed ?? []) {
    const v = inputData[field];
    if (typeof v === 'number') computed[field] = v;
  }
  gates.push(...runItemGates({ output, guards: resolved, computed }));
  if (guards.faq_shape !== undefined) {
    gates.push(gateFaqShape(output, resolved.faq_shape as { count?: number; answers_must_contain?: string[] }));
  }
  return gates;
}

/**
 * Generate exactly ONE item: cache check → prompt build (constraint notes are
 * NOT optional) → forced strict tool call → coerce → gates → persist status.
 */
export async function generateItem(deps: GenerateDeps, req: GenerateRequest): Promise<{
  ok: boolean;
  status?: string;
  cached?: boolean;
  item_key?: string;
  error?: string;
  gates?: GateResult[];
}> {
  const item = await deps.getItem(req.item_id);
  if (!item) return { ok: false, error: `item ${req.item_id} not found` };

  // Cache: the row's data_hash was computed from input_data at job creation.
  // A regenerated fact-set produces a different hash → a different row →
  // this row would have been superseded. Same row + already generated output
  // + not regenerate mode → return cached, zero LLM tokens.
  const expectedHash = await dataHash(item.input_data);
  if (expectedHash !== item.data_hash) {
    return { ok: false, error: `data_hash drift on ${item.item_key}: stored input_data no longer matches its hash` };
  }
  if (item.output && item.status !== 'pending' && req.mode !== 'regenerate') {
    return { ok: true, status: item.status, cached: true, item_key: item.item_key };
  }

  const template = await deps.getTemplate(item.site_id, item.template_key, item.template_version);
  if (!template) {
    return { ok: false, error: `template ${item.template_key} v${item.template_version} not found` };
  }

  // Site persona fetched ONCE per invocation — both attempts of a P1 retry are
  // guaranteed to run under the same doctrine.
  const persona = await deps.getSitePersona(item.site_id);

  let llmReq = buildItemLlmRequest(item, template, { persona });
  let llmResult = await deps.llm(llmReq);

  // P1: a truncated (max_tokens) or degenerate (stub/duplicate shell) result is
  // the dominant failure at scale — retry ONCE with more headroom before letting
  // the gates fail it into the manual-regen queue. The wasted attempt is still
  // billed at job level.
  if (llmResult.stopReason === 'max_tokens' || isDegenerate(llmResult.output)) {
    const wasted = llmResult;
    llmReq = buildItemLlmRequest(item, template, { maxTokensOverride: bumpMaxTokens(llmReq.maxTokens), persona });
    llmReq.temperature = Math.min(1, template.temperature + 0.2);
    llmResult = await deps.llm(llmReq);
    // Fold the wasted attempt's tokens into the result so finalizeItemFromLlm
    // bills BOTH attempts to the job accumulator AND the item row (one code path,
    // no double-count).
    llmResult = {
      ...llmResult,
      tokensIn: llmResult.tokensIn + wasted.tokensIn,
      tokensOut: llmResult.tokensOut + wasted.tokensOut,
    };
  }

  const { status, gates } = await finalizeItemFromLlm(deps, item, template, llmResult, {
    mode: req.mode,
    usageChannel: 'sync',
    personaHash: await personaHashOf(persona),
  });

  return { ok: true, status, cached: false, item_key: item.item_key, gates };
}

/**
 * Dry-run: build prompt, call LLM, run per-item gates — no DB writes.
 * Used by Template Studio test panel via prose-admin proxy.
 */
export async function dryRunTemplate(
  deps: Pick<GenerateDeps, 'llm'>,
  req: DryRunRequest,
): Promise<{
  ok: boolean;
  output?: Record<string, unknown>;
  gates?: GateResult[];
  tokens_in?: number;
  tokens_out?: number;
  error?: string;
}> {
  const template = req.template;
  const itemKey = req.item_key ?? 'dry-run';

  try {
    const llmReq = buildItemLlmRequest(
      { item_key: itemKey, input_data: req.input_data },
      template,
      { persona: req.persona },
    );
    const llmResult = await deps.llm(llmReq);
    const output = coerceToSchema(llmResult.output, template.output_schema) as Record<string, unknown>;
    const gates = assembleItemGates(output, template, req.input_data);
    return {
      ok: true,
      output,
      gates,
      tokens_in: llmResult.tokensIn,
      tokens_out: llmResult.tokensOut,
    };
  } catch (err) {
    return { ok: false, error: String(err instanceof Error ? err.message : err) };
  }
}

// ── Anthropic Message Batches ────────────────────────────────────────────────

export interface JobBatchRow {
  id: string;
  mode: string;
  anthropic_batch_id: string | null;
  batch_status: string | null;
}

export interface BatchDeps extends GenerateDeps {
  getJob(jobId: string): Promise<JobBatchRow | null>;
  listPendingItems(jobId: string): Promise<ItemRow[]>;
  countPending(jobId: string): Promise<number>;
  updateJobBatch(jobId: string, patch: {
    anthropic_batch_id?: string | null;
    batch_status?: string | null;
    status?: string;
    run_channel?: string;
  }): Promise<void>;
  /** Leave item pending but record why a batch custom failed. */
  noteBatchFailure(item: ItemRow, error: string): Promise<void>;
  /** Leave item pending, bump its gen_retry counter, so the next submit re-issues
   *  it with a larger max_tokens (P1 batch-aware retry). */
  noteBatchRetry(item: ItemRow, detail: string): Promise<void>;
}

/** Opaque Anthropic messages.create params — typed in index.ts where the SDK lives. */
export type AnthropicMessageParams = object;

export interface AnthropicBatchApi {
  create(requests: Array<{ custom_id: string; params: AnthropicMessageParams }>): Promise<{ id: string }>;
  retrieve(batchId: string): Promise<{
    processing_status: string;
    request_counts?: {
      processing?: number;
      succeeded?: number;
      errored?: number;
      canceled?: number;
      expired?: number;
    };
  }>;
  results(batchId: string): AsyncIterable<{
    custom_id: string;
    result: {
      type: string;
      message?: {
        content: Array<{ type: string; name?: string; input?: unknown }>;
        usage: { input_tokens: number; output_tokens: number };
        stop_reason?: string | null;
      };
      error?: { message?: string };
    };
  }>;
}

/** Parse a succeeded batch result into LlmResult (emit_content tool input + tokens). */
export function llmResultFromBatchMessage(message: {
  content: Array<{ type: string; name?: string; input?: unknown }>;
  usage: { input_tokens: number; output_tokens: number };
  stop_reason?: string | null;
}): LlmResult | null {
  const toolUse = message.content.find(
    (b) => b.type === 'tool_use' && b.name === 'emit_content',
  );
  if (!toolUse?.input) return null;
  return {
    output: toolUse.input,
    tokensIn: message.usage.input_tokens,
    tokensOut: message.usage.output_tokens,
    stopReason: message.stop_reason ?? undefined,
  };
}

export async function submitBatchJob(
  deps: BatchDeps,
  batchApi: AnthropicBatchApi,
  toMessageParams: (req: LlmRequest) => AnthropicMessageParams,
  jobId: string,
): Promise<{
  ok: boolean;
  batch_id?: string;
  request_count?: number;
  remaining?: number;
  batch_status?: string;
  error?: string;
}> {
  const job = await deps.getJob(jobId);
  if (!job) return { ok: false, error: `job ${jobId} not found` };
  if (job.anthropic_batch_id) {
    return { ok: false, error: 'batch already submitted — use collect_batch' };
  }

  const pending = await deps.listPendingItems(jobId);
  if (pending.length === 0) {
    const remaining = await deps.countPending(jobId);
    return { ok: true, request_count: 0, remaining };
  }

  const templateCache = new Map<string, TemplateRow | null>();
  // Site persona, cached per site like templates (a job's items share a site
  // today, but the idiom costs nothing and stays correct if that changes).
  const personaCache = new Map<string, string | null>();
  const requests: Array<{ custom_id: string; params: AnthropicMessageParams }> = [];

  for (const item of pending) {
    const tplKey = `${item.site_id}:${item.template_key}:${item.template_version}`;
    let template = templateCache.get(tplKey);
    if (template === undefined) {
      template = await deps.getTemplate(item.site_id, item.template_key, item.template_version);
      templateCache.set(tplKey, template);
    }
    if (!template) continue;

    let persona = personaCache.get(item.site_id);
    if (persona === undefined) {
      persona = await deps.getSitePersona(item.site_id);
      personaCache.set(item.site_id, persona);
    }

    const expectedHash = await dataHash(item.input_data);
    if (expectedHash !== item.data_hash) continue;

    // P1: an item re-queued after a truncated/degenerate batch result gets a
    // bumped budget so the retry doesn't hit the same wall.
    const retry = Number((item.validation as { gen_retry?: unknown } | null)?.gen_retry ?? 0);
    const override = retry > 0 ? bumpMaxTokens(sizedMaxTokens(template), 1 + 0.7 * retry) : undefined;
    const llmReq = buildItemLlmRequest(item, template, { maxTokensOverride: override, persona });
    requests.push({ custom_id: item.id, params: toMessageParams(llmReq) });
  }

  if (requests.length === 0) {
    const remaining = await deps.countPending(jobId);
    return { ok: true, request_count: 0, remaining };
  }

  const batch = await batchApi.create(requests);
  await deps.updateJobBatch(jobId, {
    anthropic_batch_id: batch.id,
    batch_status: 'in_progress',
    status: 'running',
    run_channel: 'batch',
  });

  const remaining = await deps.countPending(jobId);
  return {
    ok: true,
    batch_id: batch.id,
    request_count: requests.length,
    remaining,
    batch_status: 'in_progress',
  };
}

export async function collectBatchJob(
  deps: BatchDeps,
  batchApi: AnthropicBatchApi,
  jobId: string,
): Promise<{
  ok: boolean;
  batch_status?: string;
  request_counts?: AnthropicBatchApi['retrieve'] extends (...args: never) => Promise<infer R> ? R extends { request_counts?: infer C } ? C : never : never;
  processed?: number;
  remaining?: number;
  failures?: number;
  error?: string;
}> {
  const job = await deps.getJob(jobId);
  if (!job) return { ok: false, error: `job ${jobId} not found` };
  if (!job.anthropic_batch_id) return { ok: false, error: 'no anthropic_batch_id on job' };

  const batch = await batchApi.retrieve(job.anthropic_batch_id);
  await deps.updateJobBatch(jobId, { batch_status: batch.processing_status });

  if (batch.processing_status !== 'ended') {
    const remaining = await deps.countPending(jobId);
    return {
      ok: true,
      batch_status: batch.processing_status,
      request_counts: batch.request_counts,
      remaining,
      processed: 0,
      failures: 0,
    };
  }

  const templateCache = new Map<string, TemplateRow | null>();
  // Persona hash for the audit stamp. Fetched at collect time: if the persona
  // changed while the batch was in flight (rare; load-seed warns loudly and
  // recommends a version bump) the stamp reflects the newer doctrine — an
  // accepted v1 approximation.
  const personaHashCache = new Map<string, string | null>();
  let processed = 0;
  let failures = 0;
  const mode = job.mode === 'regenerate' ? 'regenerate' as const : 'generate' as const;

  for await (const row of batchApi.results(job.anthropic_batch_id)) {
    const item = await deps.getItem(row.custom_id);
    if (!item || item.status !== 'pending') continue;

    if (row.result.type === 'succeeded' && row.result.message) {
      const llmResult = llmResultFromBatchMessage(row.result.message);
      if (!llmResult) {
        failures++;
        await deps.noteBatchFailure(item, 'succeeded result missing emit_content tool_use');
        continue;
      }
      const tplKey = `${item.site_id}:${item.template_key}:${item.template_version}`;
      let template = templateCache.get(tplKey);
      if (template === undefined) {
        template = await deps.getTemplate(item.site_id, item.template_key, item.template_version);
        templateCache.set(tplKey, template);
      }
      if (!template) {
        failures++;
        await deps.noteBatchFailure(item, `template ${item.template_key} v${item.template_version} not found`);
        continue;
      }
      // P1 (batch): a truncated/degenerate succeeded result is NOT finalized —
      // bill the wasted attempt, leave the item pending with a bumped gen_retry,
      // and let the next drain re-submit it with a larger budget. Capped at 1
      // retry; after that it finalizes and the gates fail it terminally.
      const retry = Number((item.validation as { gen_retry?: unknown } | null)?.gen_retry ?? 0);
      if ((llmResult.stopReason === 'max_tokens' || isDegenerate(llmResult.output)) && retry < 1) {
        await deps.addJobUsage(item.job_id, llmResult.tokensIn, llmResult.tokensOut, 'batch');
        await deps.noteBatchRetry(item,
          `${llmResult.stopReason === 'max_tokens' ? 'truncated' : 'degenerate'} — requeued (retry ${retry + 1})`);
        continue;
      }
      let personaHash = personaHashCache.get(item.site_id);
      if (personaHash === undefined) {
        personaHash = await personaHashOf(await deps.getSitePersona(item.site_id));
        personaHashCache.set(item.site_id, personaHash);
      }
      await finalizeItemFromLlm(deps, item, template, llmResult, { mode, usageChannel: 'batch', personaHash });
      processed++;
    } else {
      failures++;
      const detail = row.result.type === 'errored'
        ? (row.result.error?.message ?? 'errored')
        : row.result.type === 'expired'
        ? 'expired'
        : row.result.type === 'canceled'
        ? 'canceled'
        : `batch result type=${row.result.type}`;
      await deps.noteBatchFailure(item, detail);
    }
  }

  await deps.updateJobBatch(jobId, {
    anthropic_batch_id: null,
    batch_status: 'ended',
  });

  const remaining = await deps.countPending(jobId);
  return {
    ok: true,
    batch_status: 'ended',
    request_counts: batch.request_counts,
    processed,
    remaining,
    failures,
  };
}
