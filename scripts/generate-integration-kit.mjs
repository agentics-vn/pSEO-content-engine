/**
 * generate-integration-kit — Seat 2, shipped from the engine.
 *
 *   node scripts/generate-integration-kit.mjs seeds/<client> <out-dir>
 *
 * Reads a tenant seed (site.json + template.*.json) and emits the starter kit
 * the site integrator drops into the CLIENT repo: a generic pull-and-throw
 * script, a page-component stub with one section per schema field, FAQPage/
 * WebSite JSON-LD helpers, and the integration checklist. The engine stays
 * headless — this generates code that runs in the consuming app, it never
 * couples the engine to any client's stack.
 */

import { mkdir, readFile, readdir, writeFile } from 'node:fs/promises';
import path from 'node:path';

const [seedDir, outDir] = process.argv.slice(2);
if (!seedDir || !outDir) {
  console.error('usage: generate-integration-kit.mjs seeds/<client> <out-dir>');
  process.exit(1);
}

const site = JSON.parse(await readFile(path.join(seedDir, 'site.json'), 'utf8'));
const templateFile = (await readdir(seedDir)).find((f) => f.startsWith('template.') && f.endsWith('.json'));
if (!templateFile) throw new Error(`no template.*.json in ${seedDir}`);
const template = JSON.parse(await readFile(path.join(seedDir, templateFile), 'utf8'));

const SLUG = site.slug;
const KEY = template.key;
const KEY_ENV = `${SLUG.toUpperCase().replace(/-/g, '_')}_CONTENT_KEY`;
const props = template.output_schema?.properties ?? {};
const stringFields = Object.entries(props).filter(([, d]) => d.type === 'string').map(([k]) => k);
const hasFaqs = props.faqs?.type === 'array';

const write = async (rel, content) => {
  const p = path.join(outDir, rel);
  await mkdir(path.dirname(p), { recursive: true });
  await writeFile(p, content);
  console.log(`  ${rel}`);
};

// ── pull script (generic: declared pages, NFC assert, throw-on-missing) ─────
await write('scripts/pull-content.mjs', `/**
 * Pull published "${KEY}" content for ${SLUG} — run in prebuild.
 * Generated by pseo-content-engine generate-integration-kit; edit freely.
 *
 *   ENGINE_URL=… ${KEY_ENV}=… node scripts/pull-content.mjs expected-pages.json
 *
 * THROWS (never ships a partial site) when a declared page is missing from
 * the pull, any string is not NFC, or the optional fact check fails.
 */
import { writeFile, mkdir, readFile } from 'node:fs/promises';
import path from 'node:path';

const ENGINE_URL = process.env.ENGINE_URL;
const KEY = process.env.${KEY_ENV};
const OUT_FILE = process.env.CONTENT_OUT ?? 'src/data/${KEY}.generated.json';
if (!ENGINE_URL || !KEY) throw new Error('[pull] ENGINE_URL and ${KEY_ENV} are required');

// Declare exactly which pages MUST exist — a partial pull fails the build loud.
const expected = JSON.parse(await readFile(process.argv[2] ?? 'expected-pages.json', 'utf8'));

const res = await fetch(\`\${ENGINE_URL}/v1/sites/${SLUG}/published?template=${KEY}\`, {
  headers: { authorization: \`Bearer \${KEY}\` },
});
if (!res.ok) throw new Error(\`[pull] engine returned \${res.status}: \${await res.text()}\`);
const rows = await res.json();

const assertNfc = (v, where) => {
  if (typeof v === 'string' && v.normalize('NFC') !== v) throw new Error(\`[pull] non-NFC string in \${where}\`);
  if (Array.isArray(v)) v.forEach((x, i) => assertNfc(x, \`\${where}[\${i}]\`));
  else if (v && typeof v === 'object') for (const [k, x] of Object.entries(v)) assertNfc(x, \`\${where}.\${k}\`);
};

/**
 * OPTIONAL fact-drift check: recompute the facts this page stands on and
 * assert the prose agrees (see architecture §10). Fill in per vertical.
 */
function assertFacts(itemKey, output) { /* e.g. price/date/number cross-check */ }

const byKey = new Map();
for (const row of rows) {
  assertNfc(row.output, row.item_key);
  assertFacts(row.item_key, row.output);
  byKey.set(row.item_key, row);
}
const missing = expected.item_keys.filter((k) => !byKey.has(k));
if (missing.length) {
  throw new Error(\`[pull] \${missing.length} declared page(s) missing — refusing a partial build:\\n\` + missing.join('\\n'));
}

await mkdir(path.dirname(OUT_FILE), { recursive: true });
await writeFile(OUT_FILE, JSON.stringify([...byKey.values()], null, 2) + '\\n');
console.log(\`[pull] wrote \${byKey.size} pages (\${expected.item_keys.length} declared) → \${OUT_FILE}\`);
`);

await write('expected-pages.example.json', JSON.stringify({
  _comment: 'Declare every page that must exist. Widen per rollout phase.',
  item_keys: ['example-item-key-1', 'example-item-key-2'],
}, null, 2) + '\n');

// ── JSON-LD helpers ──────────────────────────────────────────────────────────
