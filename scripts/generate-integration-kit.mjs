// ── Auto-indexing pipeline (architecture §12) ────────────────────────────────
const DOMAIN = site.domain ?? `${SLUG}.example`;

await write('scripts/submit-sitemap.mjs', `/**
 * Submit the sitemap to Google Search Console + push URLs to IndexNow.
 * Generated by pseo-content-engine generate-integration-kit.
 *
 * NOTE: Google removed the anonymous sitemap ping endpoint (Jan 2024) —
 * submission REQUIRES Search Console API auth. Auth resolves via Application
 * Default Credentials: either google-github-actions/auth (WIF, preferred —
 * no long-lived keys) or GOOGLE_APPLICATION_CREDENTIALS pointing at a
 * service-account JSON whose account was added as a user on the GSC property.
 *
 *   GSC_PROPERTY='sc-domain:${DOMAIN}' SITEMAP_URL='https://${DOMAIN}/sitemap-index.xml' \\
 *   INDEXNOW_KEY=… node scripts/submit-sitemap.mjs
 */
import { GoogleAuth } from 'google-auth-library';

const PROPERTY = process.env.GSC_PROPERTY ?? 'sc-domain:${DOMAIN}';
const SITEMAP = process.env.SITEMAP_URL ?? 'https://${DOMAIN}/sitemap-index.xml';
const INDEXNOW_KEY = process.env.INDEXNOW_KEY; // also served at /<key>.txt (put the file in public/)

// 1. GSC: PUT sitemaps.submit
const auth = new GoogleAuth({ scopes: ['https://www.googleapis.com/auth/webmasters'] });
const client = await auth.getClient();
await client.request({
  method: 'PUT',
  url: \`https://www.googleapis.com/webmasters/v3/sites/\${encodeURIComponent(PROPERTY)}/sitemaps/\${encodeURIComponent(SITEMAP)}\`,
});
console.log(\`[gsc] submitted \${SITEMAP} for \${PROPERTY}\`);

// 2. IndexNow (Bing/Yandex/Naver — Google does NOT consume IndexNow)
if (INDEXNOW_KEY) {
  const xml = await (await fetch(SITEMAP)).text();
  // sitemap index → fetch children; plain sitemap → use directly
  const locs = [...xml.matchAll(/<loc>([^<]+)<\\/loc>/g)].map((m) => m[1]);
  const urls = [];
  for (const loc of locs) {
    if (loc.endsWith('.xml')) {
      const child = await (await fetch(loc)).text();
      urls.push(...[...child.matchAll(/<loc>([^<]+)<\\/loc>/g)].map((m) => m[1]));
    } else urls.push(loc);
  }
  const host = new URL(SITEMAP).hostname;
  const res = await fetch('https://api.indexnow.org/indexnow', {
    method: 'POST',
    headers: { 'content-type': 'application/json; charset=utf-8' },
    body: JSON.stringify({
      host,
      key: INDEXNOW_KEY,
      keyLocation: \`https://\${host}/\${INDEXNOW_KEY}.txt\`,
      urlList: urls.slice(0, 10000),
    }),
  });
  console.log(\`[indexnow] pushed \${Math.min(urls.length, 10000)} urls → \${res.status}\`);
} else {
  console.log('[indexnow] INDEXNOW_KEY not set — skipped');
}
`);

await write('scripts/index-coverage-report.mjs', `/**
 * Weekly index-coverage report (architecture §12): inspect a sample of
 * sitemap URLs via the URL Inspection API and report the indexed %.
 * THIS NUMBER — not generation capacity — gates the next content batch.
 * Same ADC auth as submit-sitemap.mjs. Quota: 2000 inspections/day/property.
 */
import { GoogleAuth } from 'google-auth-library';

const PROPERTY = process.env.GSC_PROPERTY ?? 'sc-domain:${DOMAIN}';
const SITEMAP = process.env.SITEMAP_URL ?? 'https://${DOMAIN}/sitemap-index.xml';
const SAMPLE = Number(process.env.COVERAGE_SAMPLE ?? 80);

const xml = await (await fetch(SITEMAP)).text();
const locs = [...xml.matchAll(/<loc>([^<]+)<\\/loc>/g)].map((m) => m[1]);
const urls = [];
for (const loc of locs) {
  if (loc.endsWith('.xml')) {
    const child = await (await fetch(loc)).text();
    urls.push(...[...child.matchAll(/<loc>([^<]+)<\\/loc>/g)].map((m) => m[1]));
  } else urls.push(loc);
}
// Deterministic spread across the list, not just the head.
const step = Math.max(1, Math.floor(urls.length / SAMPLE));
const sample = urls.filter((_, i) => i % step === 0).slice(0, SAMPLE);

const auth = new GoogleAuth({ scopes: ['https://www.googleapis.com/auth/webmasters'] });
const client = await auth.getClient();
let indexed = 0;
const notIndexed = [];
for (const url of sample) {
  const { data } = await client.request({
    method: 'POST',
    url: 'https://searchconsole.googleapis.com/v1/urlInspection/index:inspect',
    data: { inspectionUrl: url, siteUrl: PROPERTY },
  });
  const state = data.inspectionResult?.indexStatusResult?.coverageState ?? 'unknown';
  if (state === 'Submitted and indexed') indexed++;
  else notIndexed.push(\`\${url} — \${state}\`);
}

const pct = Math.round((indexed / sample.length) * 100);
const lines = [
  \`# Index coverage: \${pct}% (\${indexed}/\${sample.length} sampled of \${urls.length})\`,
  '',
  ...notIndexed.slice(0, 25).map((l) => \`- \${l}\`),
];
console.log(lines.join('\\n'));
if (process.env.GITHUB_STEP_SUMMARY) {
  const { appendFileSync } = await import('node:fs');
  appendFileSync(process.env.GITHUB_STEP_SUMMARY, lines.join('\\n') + '\\n');
}
`);

